					</svg>
				</button>
			</div>

			<!-- Error message (e.g., webview detection) -->
			{#if $authError}
				<div class="mb-4 p-3 bg-amber-50 dark:bg-amber-900/30 border border-amber-200 dark:border-amber-700 rounded-lg">
					<p class="text-sm text-amber-800 dark:text-amber-200">{$authError}</p>
				</div>
			{/if}

			<!-- Sign-in buttons -->
			<div class="space-y-3">
				<button
					on:click={handleAppleLogin}
					class="w-full flex items-center justify-center gap-3 px-4 py-3 bg-black hover:bg-gray-900 text-white rounded-lg transition-colors"
